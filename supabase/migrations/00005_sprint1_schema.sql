-- ── 1. display_id_counters ───────────────────────────────────
-- Atomic counter per (organization, prefix). Guarantees unique,
-- sequential, gap-free IDs for each entity type per org.
-- Uses ON CONFLICT DO UPDATE for concurrency safety.

create table if not exists display_id_counters (
  organization_id  uuid    not null references organizations(id) on delete cascade,
  id_prefix        text    not null,  -- 'S', 'F', 'H', 'P', 'T', 'A', 'V', 'ST', 'RG'
  last_value       integer not null default 0,
  primary key (organization_id, id_prefix)
);

comment on table display_id_counters is
  'Atomic counters for generating human-readable SchoolCo display IDs (e.g. RLA-S0001). One row per org per prefix.';

-- ── 2. next_org_display_id() ─────────────────────────────────
-- Atomically increments the counter and returns the next display ID.
-- Format: {ORG_SHORT_NAME}-{PREFIX}{ZERO_PADDED_4_DIGIT_NUMBER}
-- Example: RLA-S0001, RLA-F0001, RLA-H0001
--
-- SECURITY DEFINER so it can bypass RLS on display_id_counters.
-- Only callable from server-side triggers and server actions.

create or replace function next_org_display_id(
  p_organization_id  uuid,
  p_prefix           text
) returns text
language plpgsql
security definer
as $$
declare
  v_short_name  text;
  v_next        integer;
begin
  -- Get the org short name for the ID prefix
  select coalesce(short_name, 'ORG')
  into   v_short_name
  from   organizations
  where  id = p_organization_id;

  -- Atomically increment the counter (safe under concurrent inserts)
  insert into display_id_counters (organization_id, id_prefix, last_value)
  values (p_organization_id, p_prefix, 1)
  on conflict (organization_id, id_prefix) do update
    set last_value = display_id_counters.last_value + 1
  returning last_value into v_next;

  -- Return formatted ID: "RLA-S0001"
  return upper(v_short_name) || '-' || p_prefix || lpad(v_next::text, 4, '0');
end;
$$;

comment on function next_org_display_id(uuid, text) is
  'Atomically generates the next display ID for an org+prefix combination. Returns e.g. RLA-S0001.';

-- ── 3. families ──────────────────────────────────────────────
-- A family is the top-level grouping for students and guardians.
-- One family can have multiple households (for split-household support).
-- Students belong to a family. Guardians are linked to students
-- via the guardianships table (and optionally to a household).

create table if not exists families (
  -- Standard SchoolCo columns
  id               uuid         default uuid_generate_v4() primary key,
  organization_id  uuid         not null references organizations(id) on delete cascade,
  created_at       timestamptz  default now() not null,
  created_by       uuid         references profiles(id),
  updated_at       timestamptz  default now() not null,
  updated_by       uuid         references profiles(id),
  status           text         default 'active' not null,
  archived_at      timestamptz,
  archived_by      uuid         references profiles(id),

  -- Human-readable display ID — auto-generated by trigger
  family_display_id  text       unique,  -- e.g. "RLA-F0001"

  -- Family info
  family_name        text       not null,  -- e.g. "The Thompson Family"
  notes              text,                 -- Staff notes — never shown to parents
  is_split_household boolean    not null default false  -- Flag: multiple households
);

comment on table families is
  'Top-level family entity. Each family can have one or more households and multiple students.';
comment on column families.family_display_id is
  'Human-readable ID, auto-generated on insert. Format: {ORG}-F{NNNN}. Used in search, reports, QR badges.';
comment on column families.notes is
  'Staff-only notes about this family. Never exposed to parent or guardian sessions.';
comment on column families.is_split_household is
  'True if this family has guardians in separate households with different custody arrangements.';

create index idx_families_org         on families(organization_id);
create index idx_families_status      on families(organization_id, status) where archived_at is null;
create index idx_families_display_id  on families(family_display_id);

-- Auto-update updated_at
create trigger families_updated_at
  before update on families
  for each row execute function update_updated_at_column();

-- Auto-generate family_display_id on insert
create or replace function set_family_display_id()
returns trigger
language plpgsql
security definer
as $$
begin
  if new.family_display_id is null then
    new.family_display_id := next_org_display_id(new.organization_id, 'F');
  end if;
  return new;
end;
$$;

create trigger families_display_id
  before insert on families
  for each row execute function set_family_display_id();

-- ── 4. households ────────────────────────────────────────────
-- A household represents one physical address/home unit within a family.
-- Most families have one household. Split-household families have two or more.
-- Each household has its own address, phone, and email.
-- Guardians are associated with a household via the guardianships.household_id FK.

create table if not exists households (
  -- Standard SchoolCo columns
  id               uuid         default uuid_generate_v4() primary key,
  organization_id  uuid         not null references organizations(id) on delete cascade,
  created_at       timestamptz  default now() not null,
  created_by       uuid         references profiles(id),
  updated_at       timestamptz  default now() not null,
  updated_by       uuid         references profiles(id),
  status           text         default 'active' not null,
  archived_at      timestamptz,
  archived_by      uuid         references profiles(id),

  -- Human-readable display ID — auto-generated by trigger
  household_display_id  text    unique,  -- e.g. "RLA-H0001"

  -- Household relationship
  family_id        uuid         not null references families(id) on delete cascade,
  household_label  text         not null,  -- e.g. "Johnson Family – Primary"
  sort_order       smallint     default 1,  -- 1 = primary, 2 = secondary, etc.

  -- Contact info for this household (separate from individual guardian contacts)
  address_json     jsonb,       -- {street1, street2?, city, state, zip, country}
  phone            text,
  email            text
);

comment on table households is
  'One physical address unit within a family. Supports split-household custody arrangements.';
comment on column households.household_display_id is
  'Human-readable ID, auto-generated on insert. Format: {ORG}-H{NNNN}.';
comment on column households.sort_order is
  '1 = primary household, 2 = secondary, etc. Determines display order in staff views.';
comment on column households.address_json is
  'Physical address of this household. JSON: {street1, street2?, city, state, zip, country}.';

create index idx_households_org       on households(organization_id);
create index idx_households_family    on households(family_id);
create index idx_households_display   on households(household_display_id);

-- Auto-update updated_at
