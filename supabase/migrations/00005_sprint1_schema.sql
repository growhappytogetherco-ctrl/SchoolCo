-- Role-to-prefix mapping for organization_members display IDs
create or replace function member_id_prefix(p_role user_role)
returns text
language sql
immutable
as $$
  select case p_role
    when 'parent'         then 'P'
    when 'teacher'        then 'T'
    when 'admin'          then 'A'
    when 'full_admin'     then 'A'
    when 'platform_admin' then 'A'
    when 'volunteer'      then 'V'
    when 'staff'          then 'ST'
    when 'registrar'      then 'RG'
    when 'student_future' then 'SF'
    else                       'M'
  end;
$$;

comment on function member_id_prefix(user_role) is
  'Returns the display ID prefix for a given role. P=Parent, T=Teacher, A=Admin, V=Volunteer, etc.';

-- Auto-generate display_id for organization_members on insert
create or replace function set_member_display_id()
returns trigger
language plpgsql
security definer
as $$
declare
  v_prefix text;
begin
  if new.display_id is null then
    v_prefix := member_id_prefix(new.role);
    new.display_id := next_org_display_id(new.organization_id, v_prefix);
  end if;
  return new;
end;
$$;

create trigger org_members_display_id
  before insert on organization_members
  for each row execute function set_member_display_id();

